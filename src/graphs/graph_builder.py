from langgraph.graph import StateGraph, START, END
from src.llms.groqllm import GroqLLM
from src.states.blogstare import BlogState
from src.nodes.blog_node import BlogNode

class GraphBuilder:
    def __init__(self,llm):
        self.llm=llm
        self.graph = StateGraph(BlogState)
        
    def build_topic_graph(self):
        """
        Build a graph to generate the topic
        """
        
        self.blog_node = BlogNode(self.llm)
        ##Nodes
        self.graph.add_node("title_creation", self.blog_node.title_creation)
        self.graph.add_node("content_generater", self.blog_node.content_generation)
        
        #edges
        self.graph.add_edge(START, "title_creation")
        self.graph.add_edge("title_creation", "content_generater")
        self.graph.add_edge("content_generater", END)
        
        return self.graph
    
    def setup_graph(self, usecase):
        if usecase == "topic":
            self.build_topic_graph()
            
        return self.graph.compile()
    
    
# Below code is for the langsmith studio
llm = GroqLLM().get_llm()

# get the graph
graph_builder = GraphBuilder(llm)
graph = graph_builder.build_topic_graph().compile()